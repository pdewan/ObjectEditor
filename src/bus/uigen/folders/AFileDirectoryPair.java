package bus.uigen.folders;

public class AFileDirectoryPair  {
	String fileName;
	String directoryName;
	public String getFileName() {
		return fileName;
	}
	public void setFileName (String newVal) {
		fileName = newVal;
	}
	public String getDirectoryName() {
		return directoryName;
	}
	public void setDirectoryName (String newVal) {
		directoryName = newVal;
	}
}